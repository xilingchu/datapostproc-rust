// ─── shared: instantaneous-snapshot ensemble ──────────────────────────────────

/// Build the full snapshot list from explicit templates plus an optional
/// `--pattern`/`--range` series.  Pattern entries have their `{t}` placeholder
/// replaced by each timestep `START, START+STEP, …, ≤ END`; the component
/// placeholder `{}` is left for [`inst_path`].
fn snapshot_templates(
    files: &[String],
    pattern: &Option<String>,
    range: &Option<Vec<usize>>,
) -> Vec<String> {
    let mut out: Vec<String> = files.to_vec();
    match (pattern, range) {
        (Some(p), Some(r)) => {
            assert!(p.contains("{t}"), "--pattern must contain the time placeholder `{{t}}`");
            let (start, end, step) = (r[0], r[1], r[2]);
            assert!(step > 0, "--range STEP must be > 0");
            assert!(start <= end, "--range START ({start}) must be ≤ END ({end})");
            let mut t = start;
            while t <= end {
                out.push(p.replace("{t}", &t.to_string()));
                t += step;
            }
        }
        (Some(_), None) => panic!("--pattern requires --range START END STEP"),
        (None, Some(_)) => panic!("--range requires --pattern"),
        (None, None) => {}
    }
    assert!(
        !out.is_empty(),
        "no snapshots given; use -f <templates> and/or --pattern + --range"
    );
    out
}

/// Resolve the file that holds component `comp` for a snapshot `template`.
///
/// If the template contains `{}` it is replaced by the component letter
/// (`u`/`v`/`w`/`p`), e.g. `inst_400000_{}.h5` → `inst_400000_u.h5`.  Otherwise
/// every component is read from the same (single-file) snapshot.
fn inst_path(template: &str, comp: char) -> String {
    if template.contains("{}") {
        template.replace("{}", &comp.to_string())
    } else {
        template.to_string()
    }
}

/// Read dataset `name` from `path` and align its streamwise extent to `nxc`,
/// matching the `spectra`/`vortex` convention:
///   nxc      already aligned;
///   nxc + 1  inst files carry one extra outflow column at the end;
///   nxc + 2  subavg files carry one ghost column on each side.
fn read_inst_field(path: &str, name: &str, nxc: usize) -> ArrayD<f64> {
    let mut h5 = H5File::new(path).unwrap_or_else(|e| panic!("open {path}: {e}"));
    h5.add_dataset(name, None)
        .unwrap_or_else(|e| panic!("dataset '{name}' in {path}: {e}"));
    let raw = match h5.dataset(name).unwrap().read_data::<f64>().unwrap() {
        H5Data::Array(a) => a,
        H5Data::Scalar(_) => panic!("'{name}' in {path} is scalar"),
    };
    let nxf = raw.shape()[2];
    match nxf.checked_sub(nxc) {
        Some(0) => raw,
        Some(1) => raw.slice_axis(Axis(2), (0..nxc).into()).to_owned(),
        Some(2) => raw.slice_axis(Axis(2), (1..nxc + 1).into()).to_owned(),
        _ => panic!("field '{name}' x extent {nxf} incompatible with x coord length {nxc}"),
    }
}

/// Span + ensemble statistics assembled from instantaneous snapshots.
///
/// `u`/`v`/`w`/`p` are ensemble means of the primitive fields; `uu`/`uw`/`uv`
/// are ensemble means of the instantaneous *products*.  Feeding these to the
/// existing FIK/RD cores recovers the Reynolds stresses exactly, because those
/// cores form u'u' = ⟨u²⟩ − ū², u'w' = ⟨uw⟩ − ū·w̄ internally (with the
/// remaining spanwise average): identical to computing the fluctuation
/// u' = u − ū first and then u'u'.
struct InstEnsemble {
    u: ArrayD<f64>,
    v: ArrayD<f64>,   // empty ([0]) unless `with_uv`
    w: ArrayD<f64>,
    p: ArrayD<f64>,
    uu: ArrayD<f64>,
    uw: ArrayD<f64>,
    uv: ArrayD<f64>,  // empty ([0]) unless `with_uv`
    x: Array1<f64>,
    y: Array1<f64>,
    zc: Array1<f64>,
    nu: f64,
}

/// Accumulate ensemble means and second moments over the given snapshot
/// templates.  With `with_uv` also accumulates v̄ and ⟨u·v⟩ (needed for the
/// per-plane FIK spanwise terms); otherwise those are left empty to save memory.
fn accumulate_inst(files: &[String], with_uv: bool) -> InstEnsemble {
    assert!(!files.is_empty(), "need at least one snapshot file");

    let read_coord = |h5: &H5File, name: &str| -> Array1<f64> {
        match h5.coord(name)
            .unwrap_or_else(|| panic!("coord '{name}' not found"))
            .read_data::<f64>().unwrap()
        {
            H5Data::Array(a) => a.into_dimensionality::<Ix1>().unwrap(),
            H5Data::Scalar(_) => panic!("coord '{name}' is scalar"),
        }
    };

    // Coordinates / metadata from the first snapshot's u file.
    let (x, y, zc, nu) = {
        let mut h5 = H5File::new(&inst_path(&files[0], 'u'))
            .expect("failed to open first u file");
        h5.get_info().expect("failed to read DNS info");
        h5.load_coords().expect("failed to load coordinates");
        let nu = h5.info().nu.expect("'nu' not in HDF5 file");
        (read_coord(&h5, "x"), read_coord(&h5, "y"), read_coord(&h5, "zc"), nu)
    };
    let nxc = x.len();
    let empty = || ArrayD::<f64>::zeros(ndarray::IxDyn(&[0]));

    // Accumulators: [u, v, w, p, uu, uw, uv].
    let mut acc: Option<[ArrayD<f64>; 7]> = None;
    for f in files {
        let u = read_inst_field(&inst_path(f, 'u'), "u", nxc);
        let w = read_inst_field(&inst_path(f, 'w'), "w", nxc);
        let p = read_inst_field(&inst_path(f, 'p'), "p", nxc);
        let uu = &u * &u;
        let uw = &u * &w;
        let (v, uv) = if with_uv {
            let v = read_inst_field(&inst_path(f, 'v'), "v", nxc);
            let uv = &u * &v;
            (v, uv)
        } else {
            (empty(), empty())
        };

        match &mut acc {
            None => acc = Some([u, v, w, p, uu, uw, uv]),
            Some(a) => {
                a[0] += &u;  a[1] += &v;  a[2] += &w;  a[3] += &p;
                a[4] += &uu; a[5] += &uw; a[6] += &uv;
            }
        }
        eprintln!("accumulated {f}");
    }

    let mut a = acc.unwrap();
    let n = files.len() as f64;
    for arr in a.iter_mut() {
        *arr /= n;
    }
    let [u, v, w, p, uu, uw, uv] = a;
    InstEnsemble { u, v, w, p, uu, uw, uv, x, y, zc, nu }
}

/// Fold the spanwise axis (axis 1) of a `(nz, ny, nx)` field onto `nphase`
/// phase planes by averaging over the spanwise repeats: output plane `p` is the
/// mean of input planes `p, p+nphase, p+2·nphase, …`.  `ny` must be a multiple
/// of `nphase`.
fn fold_spanwise(a: &ArrayD<f64>, nphase: usize) -> ArrayD<f64> {
    let (nz, ny, nx) = (a.shape()[0], a.shape()[1], a.shape()[2]);
    assert!(
        nphase > 0 && ny % nphase == 0,
        "spanwise length {ny} not divisible by nphase {nphase}"
    );
    let mut out = ArrayD::<f64>::zeros(ndarray::IxDyn(&[nz, nphase, nx]));
    for p in 0..nphase {
        let m = a.slice(s![.., p..ny;nphase, ..])
            .mean_axis(Axis(1))
            .expect("mean over spanwise repeats");
        out.slice_mut(s![.., p, ..]).assign(&m);
    }
    out
}

// ─── fik-inst sub-command ─────────────────────────────────────────────────────

#[derive(Args)]
struct FikInstArgs {
    /// Explicit snapshot templates; `{}` is replaced by the component
    /// (u/v/w/p), e.g. `inst_400000_{}.h5`.  All snapshots (these plus any
    /// generated by --pattern/--range) are ensemble-averaged together.  A
    /// template without `{}` is read as a single file holding all components.
    #[arg(short, long, num_args = 1.., value_name = "TEMPLATE")]
    files: Vec<String>,
    /// Filename pattern with a time placeholder `{t}` and component `{}`,
    /// e.g. `inst_{t}_{}.h5`.  Expanded over --range into a snapshot series so
    /// you don't have to list every timestep.
    #[arg(long, value_name = "PATTERN")]
    pattern: Option<String>,
    /// Timestep range START END STEP (inclusive) used with --pattern, e.g.
    /// `--range 400000 900000 50000` for 11 snapshots.
    #[arg(long, num_args = 3, value_names = ["START", "END", "STEP"])]
    range: Option<Vec<usize>>,
    /// Output .dat file (default: fik_inst.dat).
    #[arg(short, long, value_name = "FILE", default_value = "fik_inst.dat")]
    output: String,
    /// Half-channel height h (default: 1.0).
    #[arg(long, default_value_t = 1.0)]
    half_height: f64,
    /// Number of points to drop from the start of the x-range (e.g. a
    /// corrupted inflow lead-in).
    #[arg(long, default_value_t = 0usize)]
    trim_start: usize,
    /// Number of points to drop from the end of the x-range (e.g. a corrupted
    /// outflow tail).
    #[arg(long, default_value_t = 0usize)]
    trim_end: usize,
    /// Treat streamwise direction as periodic for derivative stencils.
    #[arg(long, default_value_t = false)]
    periodic_x: bool,
    /// Per-plane mode: fold the spanwise direction onto the control period and
    /// write one decomposition (file `<output>_p<j>.dat`) per phase plane, with
    /// the spanwise terms cf_turb_z/cf_conv_z retained.
    #[arg(long, default_value_t = false)]
    per_plane: bool,
    /// Average over the given phase-plane indices (comma-separated, 0..nphase).
    #[arg(long, value_delimiter = ',')]
    planes: Option<Vec<usize>>,
    /// Number of spanwise grid points per control period (phase planes).  The
    /// full spanwise extent is folded onto this many phases for per-plane mode;
    /// must divide the spanwise grid size.  Default 1 (fold the whole span onto
    /// a single plane); set to the control period (e.g. 8) to resolve phases.
    #[arg(long, default_value_t = 1usize)]
    nphase: usize,
}

fn run_fik_inst(args: FikInstArgs) {
    // Phase-resolved (per-plane) mode only matters when the span is folded onto
    // more than one phase; with nphase == 1 the whole span is one plane and the
    // spanwise flux terms vanish, so we fall through to the averaged core.
    let with_uv = (args.per_plane || args.planes.is_some()) && args.nphase > 1;
    let files = snapshot_templates(&args.files, &args.pattern, &args.range);
    let ens = accumulate_inst(&files, with_uv);
    let nu = ens.nu;
    let re_b = 1.0 / nu;

    // Trim a corrupted lead-in / tail from the streamwise range.
    let nx_full = ens.x.len();
    let lo = args.trim_start;
    let hi = nx_full - args.trim_end;
    assert!(lo < hi, "trim_start ({lo}) must be < nx - trim_end ({hi})");
    let trim = |a: &ArrayD<f64>| a.slice_axis(Axis(2), (lo..hi).into()).to_owned();

    let u  = trim(&ens.u);
    let w  = trim(&ens.w);
    let p  = trim(&ens.p);
    let uu = trim(&ens.uu);
    let uw = trim(&ens.uw);
    let x  = ens.x.slice(s![lo..hi]).to_owned();
    let zc = ens.zc;

    // Direct C_f from viscous sublayer: 2ν·u(iz=1)/zc[1].
    let iz1 = 1usize;
    let cf_direct_plane = |u: &ArrayD<f64>, j: usize| -> Array1<f64> {
        u.slice(s![iz1, j, ..]).mapv(|uv| 2.0 * nu * uv / zc[iz1])
    };
    let cf_direct_mean: Array1<f64> = {
        let u_ymean: Array1<f64> = u.slice(s![iz1, .., ..])
            .mean_axis(ndarray::Axis(0))
            .expect("mean over y");
        u_ymean.mapv(|uv| 2.0 * nu * uv / zc[iz1])
    };

    if with_uv {
        // ── per-plane mode: fold the spanwise direction onto the control
        // period (`nphase` points per cycle) and decompose each phase plane.
        // Points j, j+nphase, j+2·nphase, … share the same phase, so folding
        // = phase-averaging over the spanwise repeats, matching the subavg
        // convention (which stores exactly `nphase` spanwise phases).
        let np = args.nphase;
        let v  = trim(&ens.v);
        let uv = trim(&ens.uv);
        let uf  = fold_spanwise(&u,  np);
        let vf  = fold_spanwise(&v,  np);
        let wf  = fold_spanwise(&w,  np);
        let pf  = fold_spanwise(&p,  np);
        let uuf = fold_spanwise(&uu, np);
        let uvf = fold_spanwise(&uv, np);
        let uwf = fold_spanwise(&uw, np);
        let y   = ens.y.slice(s![0..np]).to_owned();
        let decs = fik_decomposition_planes(
            &uf, &vf, &wf, &pf, &uuf, &uvf, &uwf,
            &x, &y, &zc,
            re_b, args.half_height, args.periodic_x,
        ).expect("fik_decomposition_planes failed");

        if let Some(planes) = &args.planes {
            for &j in planes {
                assert!(j < decs.len(), "plane index {j} out of range (nphase={})", decs.len());
            }
            let sel: Vec<&FikDecomposition> = planes.iter().map(|&j| &decs[j]).collect();
            let fik = fik_average(&sel).expect("fik_average failed");
            let mut cf_dir = Array1::<f64>::zeros(x.len());
            for &j in planes {
                cf_dir = cf_dir + &cf_direct_plane(&uf, j);
            }
            cf_dir /= planes.len() as f64;
            write_fik_dat(&fik, cf_dir, &x, re_b, &args.output);
        } else {
            let stem = args.output.strip_suffix(".dat").unwrap_or(&args.output).to_string();
            for (j, d) in decs.iter().enumerate() {
                write_fik_dat(d, cf_direct_plane(&uf, j), &x, re_b, &format!("{stem}_p{j}.dat"));
            }
        }
    } else {
        // ── spanwise-averaged mode (default) ─────────────────────────────────
        // v is unused by fik_decomposition (spanwise-homogeneous); pass u as a
        // shape-compatible placeholder.
        let fik = fik_decomposition(
            &u, &u, &w, &p, &uu, &uw,
            &x, &zc,
            re_b, args.half_height, args.periodic_x,
        ).expect("fik_decomposition failed");
        write_fik_dat(&fik, cf_direct_mean, &x, re_b, &args.output);
    }
}

// ─── rd-inst sub-command ──────────────────────────────────────────────────────

#[derive(Args)]
struct RdInstArgs {
    /// Explicit snapshot templates; `{}` is replaced by the component
    /// (u/v/w/p), e.g. `inst_400000_{}.h5`.  Combined with any snapshots
    /// generated by --pattern/--range and all ensemble-averaged together.
    #[arg(short, long, num_args = 1.., value_name = "TEMPLATE")]
    files: Vec<String>,
    /// Filename pattern with a time placeholder `{t}` and component `{}`,
    /// e.g. `inst_{t}_{}.h5`.  Expanded over --range into a snapshot series.
    #[arg(long, value_name = "PATTERN")]
    pattern: Option<String>,
    /// Timestep range START END STEP (inclusive) used with --pattern, e.g.
    /// `--range 400000 900000 50000` for 11 snapshots.
    #[arg(long, num_args = 3, value_names = ["START", "END", "STEP"])]
    range: Option<Vec<usize>>,
    /// Output .dat file (default: rd_inst.dat).
    #[arg(short, long, value_name = "FILE", default_value = "rd_inst.dat")]
    output: String,
    /// Half-channel height h (default: 1.0).
    #[arg(long, default_value_t = 1.0)]
    half_height: f64,
    /// Reference velocity U_ref of the RD energy frame; C_f = 2τ_w/U_ref².
    #[arg(long, default_value_t = 1.0)]
    u_ref: f64,
    /// Number of points to drop from the start of the x-range.
    #[arg(long, default_value_t = 0usize)]
    trim_start: usize,
    /// Number of points to drop from the end of the x-range.
    #[arg(long, default_value_t = 0usize)]
    trim_end: usize,
    /// Treat streamwise direction as periodic for derivative stencils.
    #[arg(long, default_value_t = false)]
    periodic_x: bool,
}

fn run_rd_inst(args: RdInstArgs) {
    let files = snapshot_templates(&args.files, &args.pattern, &args.range);
    let ens = accumulate_inst(&files, false);
    let nu = ens.nu;
    let re_b = 1.0 / nu;

    let nx_full = ens.x.len();
    let lo = args.trim_start;
    let hi = nx_full - args.trim_end;
    assert!(lo < hi, "trim_start ({lo}) must be < nx - trim_end ({hi})");
    let trim = |a: &ArrayD<f64>| a.slice_axis(Axis(2), (lo..hi).into()).to_owned();

    let u  = trim(&ens.u);
    let w  = trim(&ens.w);
    let p  = trim(&ens.p);
    let uu = trim(&ens.uu);
    let uw = trim(&ens.uw);
    let x  = ens.x.slice(s![lo..hi]).to_owned();
    let zc = ens.zc;

    let iz1 = 1usize;
    let cf_direct: Array1<f64> = {
        let u_ymean: Array1<f64> = u.slice(s![iz1, .., ..])
            .mean_axis(ndarray::Axis(0))
            .expect("mean over y");
        u_ymean.mapv(|uv| 2.0 * nu * uv / zc[iz1])
    };

    let rd = rd_decomposition(
        &u, &w, &p, &uu, &uw,
        &x, &zc,
        re_b, args.half_height, args.u_ref, args.periodic_x,
    ).expect("rd_decomposition failed");

    write_rd_dat(&rd, cf_direct, &x, re_b, &args.output);
}

