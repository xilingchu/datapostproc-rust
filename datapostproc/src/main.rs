// ─── CLI definition ───────────────────────────────────────────────────────────

#[derive(Parser)]
#[command(name = "datapostproc", about = "DNS data post-processor")]
struct Cli {
    #[command(subcommand)]
    command: Command,
}

#[derive(Subcommand)]
enum Command {
    /// Average and write statistics to a .dat file.
    Output(OutputArgs),
    /// Generate an XDMF file for Paraview.
    Hdfview(HdfviewArgs),
    /// Spanwise energy spectra and two-point correlations from
    /// instantaneous snapshots.
    Spectra(SpectraArgs),
    /// FIK skin-friction decomposition from a subavg HDF5 file.
    Fik(FikArgs),
    /// FIK skin-friction decomposition from instantaneous snapshots: the
    /// Reynolds stresses are formed on the fly from the fluctuations
    /// (u'u' = ⟨u²⟩ − ū², etc.) with span + ensemble averaging.
    FikInst(FikInstArgs),
    /// Renard–Deck (energy-budget) skin-friction decomposition from a
    /// subavg HDF5 file.
    Rd(RdArgs),
    /// Renard–Deck decomposition from instantaneous snapshots (Reynolds
    /// stresses formed on the fly, like `fik-inst`).
    RdInst(RdInstArgs),
    /// Reynolds-stress components (u'u', v'v', w'w', u'v', u'w', v'w') and
    /// TKE profiles from a subavg HDF5 file.
    Tke(TkeArgs),
    /// Reynolds-stress components and TKE profiles from instantaneous
    /// snapshots (fluctuation moments formed on the fly, like `fik-inst`).
    TkeInst(TkeInstArgs),
    /// Q and λ₂ vortex-identification fields from instantaneous snapshots,
    /// written to an HDF5 file (use `hdfview` to make an XDMF for Paraview).
    Vortex(VortexArgs),
}

